package controlador;

/**
 * Clase que suma números
 * @author Ismael
 */
public class Sumadora {
    /**
    * Constructor vacío
    */
    public Sumadora(){
        
    }
    
    /**
    * Método que realiza una suma de dos números 
    * @param a El primer número a sumar
    * @param b El segundo número a sumar
    * @return La suma de dos valores double
    */
    
    public double sumar(double a, double b){
        double suma = a + b;
        return suma;
    }
}
